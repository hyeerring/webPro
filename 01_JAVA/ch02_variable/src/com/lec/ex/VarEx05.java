package com.lec.ex;

// 기본데이터타입(기초데이터타입/소문자시작)
// 참조데이터타입(객체데이터타입/대문자시작)
public class VarEx05 {
	public static void main(String[] args) {
		// 기본데이터타입
		int i;
		i = 10;

		// 참조데이터타입(객체데이터타입)
		// 메모리에 있는 실제값(stack영역) = 변수가 저장된 주소(실제 변수 데이터는 heap영역)
		String name;
		name = "김혜린"; // 실제값 + 데이터 관리를 위한 변수들
	}
}
